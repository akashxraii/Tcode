function createStarterCode(functionName) {
  return {
    typescript:
      `function ${functionName}(...args: unknown[]): unknown {\n` +
      '  // Write your solution here\n' +
      '  return args;\n' +
      '}',
    javascript:
      `function ${functionName}(...args) {\n` +
      '  // Write your solution here\n' +
      '  return args;\n' +
      '}',
    python:
      `def ${functionName}(*args):\n` +
      '    # Write your solution here\n' +
      '    return args\n',
    java:
      'class Solution {\n' +
      `  public Object ${functionName}(Object... args) {\n` +
      '    // Write your solution here\n' +
      '    return args;\n' +
      '  }\n' +
      '}',
  };
}

function createPlaceholderTests(functionName) {
  return [
    {
      label: 'Sample Case 1',
      status: 'pending',
      input: `${functionName}(sampleInput)`,
      expected: 'sampleOutput',
      actual: 'Run code to see output',
    },
  ];
}

function createProblem({
  functionName = 'solve',
  starterCode,
  description,
  examples,
  testCases,
  summary,
  ...problem
}) {
  return {
    ...problem,
    description: description || [
      summary ||
        `Solve ${problem.title} using ${problem.topics.join(', ')} techniques.`,
      'Focus on a clean algorithm, thoughtful edge-case handling, and efficient time and space complexity.',
    ],
    examples: examples || [
      {
        input: `${functionName}(sampleInput)`,
        output: 'sampleOutput',
        explanation: 'Use the stated constraints to transform the input into the expected output.',
      },
    ],
    testCases: testCases || createPlaceholderTests(functionName),
    starterCode: starterCode || createStarterCode(functionName),
  };
}

